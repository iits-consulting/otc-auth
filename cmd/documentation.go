package cmd

import (
	"bytes"
	"os"

	"github.com/gavv/cobradoc"
	"github.com/spf13/cobra"
)

//nolint:gochecknoglobals // autogenerated and recommended by spf13/cobra
var documentationCmd = &cobra.Command{
	Use:   "documentation",
	Short: "Generates markdown documentation",
	RunE: func(cmd *cobra.Command, args []string) error {
		buf := bytes.NewBufferString("")
		if err := cobradoc.WriteDocument(buf, cmd.Root(), cobradoc.Markdown, cobradoc.Options{}); err != nil {
			return err
		}

		documentation := "./generated-documentation.md"
		return os.WriteFile(documentation, buf.Bytes(), 0o600)
	},
}

//nolint:gochecknoinits // autogenerated and recommended by spf13/cobra
func init() {
	RootCmd.AddCommand(documentationCmd)
}
