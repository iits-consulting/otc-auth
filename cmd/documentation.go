package cmd

import (
	"log"

	"github.com/spf13/cobra"
	"github.com/spf13/cobra/doc"
)

//nolint:gochecknoglobals // autogenerated and recommended by spf13/cobra
var documentationCmd = &cobra.Command{
	Use:   "documentation",
	Short: "Generates markdown documentation",
	RunE: func(cmd *cobra.Command, args []string) error {
		err := doc.GenMarkdownTree(cmd.Root(), "./generated-docs/")
		log.Println("documentation generated")
		return err
	},
}

//nolint:gochecknoinits // autogenerated and recommended by spf13/cobra
func init() {
	RootCmd.AddCommand(documentationCmd)
}
